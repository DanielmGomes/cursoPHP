<?php

	$a = 0;
	$a += 10; // $a = a + 10

	echo 'operadores de atribuição: += <br>';
	echo $a . '<br>';

	$a = 5;
	$a -= 10; // $a = a - 10

	echo 'operadores de atribuição: -= <br>';
	echo $a . '<br>';

	$a = 5;
	$a *= 10; // $a = a * 10

	echo 'operadores de atribuição: *= <br>';
	echo $a . '<br>';

	$a = 5;
	$a /= 10; // $a = a / 10

	echo 'operadores de atribuição: /= <br>';
	echo $a . '<br>';

	$a = 5;
	$a %= 10; // resto da divisao

	echo 'operadores de atribuição: %= <br>';
	echo $a . '<br>';
?>